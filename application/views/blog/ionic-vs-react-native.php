                                            <h1 id="heading10">Advantages of using Ionic:</h1>
                                            <ul>
                                                <li>Making it easy to take your code and turn it into a Progressive Web App is as simple as adding in some steps because all the code is built-in to function in a web browser.</li>
                                                <li>Ionic uses Webview, which is a tool that web developers use every day. It lets them use HTML, CSS, and JavaScript to create their applications.</li>
                                                <li>Written in JavaScript, the language provides support for most modern frameworks like React, Angular, and Vue</li>
                                            </ul>
                                            <h1 id="heading11">Disadvantages of using Ionic:</h1>
                                            <ul>
                                                <li>Performance is not good as native apps or cross-platform native apps.</li>
                                                <li>To use native features, APIs, and modules, Cordova plugin integration is required</li>
                                                <li>It cannot match with native apps with respect to looks and feels.</li>
                                            </ul>
                                            <h1 id="heading12">Advantages of using React Native:</h1>
                                            <ul>
                                                <li>Enormous community support present, which means developers can connect with other developers for resolving problems.</li>
                                                <li>Written in a common language i.e Javascript.</li>
                                                <li>It can be integrated into native apps, it is possible to make your app function smoothly as a native app.</li>
                                                <li>Builds cross platform native apps for both iOS and Android </li>
                                                <li>The React Native app is translated to native code, which has the benefit of achieving 60 frames per second. This gives the user an impression that resembles a native app</li>
                                            </ul>
                                            <h1 id="heading13">Disadvantages of the React Native:</h1>
                                            <ul>
                                                <li>React Native is still in beta, even after so many years of its creation.</li>
                                                <li>Not suitable for apps that have high-quality animations.</li>
                                                <li>Developers have to learn React before jumping to React native</li>
                                            </ul>
