                <section>
                <p>Building a product is expensive and time-consuming. With an MVP you have the option to feature your 
                product to the audience without investing in fully developing it. This helps you save money, time and 
                effort while getting valuable feedback from users.
                <br>
                In this blog, we will be discussing minimum viable product examples. To find out what is a minimum 
                viable product check our previous blog post.<br>
                The most successful startups are the ones that start 
                with a minimum viable product. They don't stay small for long, in fact, you've probably heard of 
                many of these - although I am pretty sure that when these products were MVPs, you had no idea that 
                they even existed.
                <br>
                Confused about MVPs? Check out <a href="https://designprosusa.com/blog/minimum-viable-product/"> What is a Minimum viable Product</a>?</p>
                <br>
                <h1>Some Inspiring Minimum Viable Product Examples:</h1>
                <br>
                <h3>Spotify:</h3>
                <p>The music industry was facing a lot of crises in the mid of 2000s, the presence of sites such as LimeWire 
                and The pirate Bay meant that people will never pay for music. So any music downloading or streaming platform 
                that wasn’t free will never work.
                <br>
                The owners of Spotify came up with a solution, they decided to launch a free music streaming service, 
                and use ads as a mode of revenue generation.
                <br>
                <br>
                The owners of Spotify had seen many music streaming startups fail, but why? Because those startups were 
                burning money on websites and apps without testing their idea.
                <br>
                <br>
                So the owners of Spotify built a desktop MVP, that offered one core feature i.e music streaming.
                Spotify’s MVP ran on closed beta initially, to keep the costs minimum and get valuable insights about the market.
                <br>
                It was later scaled and developed properly.</p>
                <br>
                <h3>Dropbox:</h3>
                <p>Dropbox MVP started out as a demo video MVP, it explained to the users, the benefits of storing massive data in 
                one place, this would let the users clear all the clutter and keep everything managed in one place.
                <br>
                <br>
                Dropbox received overwhelming feedback from the users, this is what made them develop the product.</p>
                <br>
                <h3>Amazon:</h3>
                <p>Amazon on the list of minimum viable product examples, surprising right?
                <br>Amazon’s story is very interesting, it started out selling books online. Bezos, the owner of Amazon had a vision that there will be 
                exponential web commerce growth in the coming years. That is why he wanted to sell stuff online and eventually chose books to start from
                <br>
                <br>
                Amazon was founded in the garage of Bezo’s home, which was rented. Initially, the website was very simple and consisted of a catalog of 
                books. Whenever a customer made a purchase on the website, Bezos used to go to the distributor, buy the book and ship it to the customer.
                <br>
                <br>
                Over the years business expanded and Amazon was able to upgrade and now Amazon is the third-largest retailer in the world.</p>
                <br>
                <h3>Foursquare:</h3>
                <p>Foursquare is a good example of a minimum viable product.
                <br>
                Initially Foursquare had just one feature. Users would check in at locations that would let them win badges.
                <br>
                It was a social platform that did not have any unique design or functionality. But now it has transformed into a comprehensive city 
                guide that has over 55 million monthly users.</p>
                <br>
                <h3>AdWords Express:</h3>
                <p>AdWords is a minimum viable product example that is actually a simple version of Adwords.
                <br>
                It generates ad copies for customers. In the beginning, the process seemed automated to the users, but in reality, the back-end consisted 
                of students who were typing the ad copies for users. Later, when AdWords express received amazing feedback, the process was automated.</p>
                <br>
                <h3>Groupon:</h3>
                <p>In the late 90s. Discount vouchers and coupons were the most trendy stuff that people were after, people wanted the best deals. 
                Groupon worked on this concept by making it easier for people to get the best deals.
                <br>
                <br>
                Groupon started as a piecemeal MVP, promoting the services offered by local businesses and offered exclusive deals that were limited for 
                a certain time.
                <br>
                The founders found building their own content management system costly and time-consuming, so they initially used a WordPress blog.
                <br>
                They scaled the model after finding out that people actually loved their product. Now Groupon operates globally and is a huge platform.</p>
                <br>
                <h3>Zappos:</h3>
                <p>Zappos is an example of Wizard of Oz MVP. The founder visited shoe shops, took photos of shoes, and pasted them on his website. When a user 
                placed an order, he shipped the shoes to the customer.
                <br/>
                This idea turned out to be a major success.</p>
                <br>
                <h3>Airbnb:</h3>
                <p>What makes Airbnb an interesting minimum viable product example is that the owners were struggling to pay the rent of their apartment. So 
                they found a solution to this by renting out their home.
                <br>
                <br>
                Though the owner’s initial idea was just to pay off the rent by renting out their home, they discovered that this model had potential. Travelers 
                were willing to stay in someone else’s room, this could save them a lot of money.
                <br>
                They created a website for the marketplace it but they struggled with acquiring customers. So they targetted Craiglist, this gave them a lot of users.
                <br>
                <br>
                Later they scaled this project and worked on its functionality.</p>
                <br>
                <h3>Facebook:</h3>
                <p>Facebook was launched at Harvard University in 2004, by Mark Zuckerberg and Eduardo Saverin, their idea was to connect students of the university 
                so they can share a mutual network together.
                <br>
                The founders of Facebook were moving strategically which involved avoiding the pitfalls like spending too much on development, scaling too early, and 
                not focusing on the feedback by users.
                <br>
                Gradually it grew all over the US and eventually took the whole world by storm.</p>
                <br>
                <h3>Etsy:</h3>
                <p>Etsy founders came up with the idea of Etsy when they were working as a freelance for crafting community forums. The members of that community 
                complained about Ebay’s high fees and complex interface.
                <br>
                eBay was the only successful marketplace back then, and all other marketplace websites failed because startups ran out of cash and were not able to 
                test properly.
                <br>
                The founders of Etsy were smart and so created an MVP website that would let anyone register and sell their stuff. The concept was a success and so Etsy’s 
                founder gradually scaled it.</p>
                <br>
                <h3>Food on the Table:</h3>
                <p>Food on the Table is a classic example of concierge MVP, it sends users food recipes, food deals, and a list of the best grocery stores. Initially, the owner 
                used to visit the house of users and manually gave them coupons, recipes, and food deliveries.
                <br>
                <br>
                After finding out that this was a huge success, he set up an automated process.</p>
                <br>
                <h3>Buffer:</h3>
                <p>Buffer is an example of landing page MVP, the startup launched landing pages. The first landing page asked the user for the user’s email address for details about 
                plans. If the users submitted the email, they were redirected to another landing page displaying the 2 paid plans and a free plan. The users had the option to 
                choose their desired plan, most people chose the paid plan which determined that it had the potential to be scaled.
                <br>
                <br>
                The social media scheduling app was then created.</p>
                <br>
                <h3>AngelList:</h3>
                <p>AnglelList is an example of MVP that is an investment and job platform. Initially, the team’s own contacts and connections were used and were contacted through emails. 
                This idea gained traction and this is the reason why the owners grew this platform eventually.</p>

