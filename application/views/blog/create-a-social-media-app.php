            <h1>Metrics you need to monitor for social media app</h1>
            <p>
            <br>For a business to be successful, it's important that you track the engagement of your social 
         media app. 
            <br> For every app, these are the metrics that you should monitor:
            <br>
            </p>
            <br>
            <h2>User engagement</h2>
            <p>
            <br>User engagement is the key KPI that will define the success or failure of your app.
            The first moment a user enters your app and the last when they exit is just one of many different 
            ways to measure engagement. Other key metrics include how long users stay on the app during 
            each session (Session Length).
            <br>
            </p>
            <br>
            <h2>Churn rate and Retention rate</h2>
            <p>
            <br>The churn rate is the statistical figure of your users who have stopped using the app. For a 
            successful app, the churn rate should always be very low.
            <br>
            <br>Retention rate is the no of users who have returned to the app after their first visit. After any 
            updates or fixing of bugs, it is important to monitor the user retention rate.
            <br>
            </p>
            <br>
            <h2>Acquisition rate </h2>
            <p>
            <br>Two of the acquisition KPIs are: CPI and CAC
            <br>
            <br>Cost Per Install(CPI) is a term used to represent the amount of money spent on advertising in 
            relation to how many installs happened. It's calculated by dividing your ad cost by the number of 
            people who installed or downloaded an app after clicking on one of your ads.
            <br>
            <br>The cost of customer acquisition(CAC) is the money spent on marketing efforts that are needed to 
            attract a client. 
            <br>
            </p>
            <br>
            <h2>User activation</h2>
            <p>
            <br>Monitor the active users in comparison to all the downloads made.
            <br>
             </p>
            <br>
            <h2>Traction</h>
            <p>
            <br>It is the most important metric to measure if you want a company's growth. It tells us about the 
            demand for your product and how that will affect it in the near future.
            <br>
            </p>
            <br>
            <h2>Burn rate</h2>
            <p>
            <br>The amount of cash that you are utilizing on your app is called the burn rate. It can include 
            marketing, server costs, employee cost, etc.
