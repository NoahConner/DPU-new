                                    <h1 id="head8">Related concepts to the minimum viable product</h1>
                                    <br/>
                                        <div class="col-sm-12 col-md-12 col-lg-12 col-xl-12 mt-12">
                                            <div class="mainListing">
                                                <!-- <div class="imgCon">
                                                    <center> <img src="<?=g('images_root')?>SVG/mvpImage3.svg" style="height:300px"></center>
                                                </div> -->
                                            </div>
